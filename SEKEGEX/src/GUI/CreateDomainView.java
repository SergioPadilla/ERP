    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        JFrame frame = new JFrame();
        String namenew = name.getText().toString();
        if (!namenew.isEmpty()){
            Server server0= new Server(server.id_server);
            if(server0.insertDomain(namenew)){
                JOptionPane.showMessageDialog(frame, "La Creacion se ha realizado con Ã©xito", "Datos Actualizados", JOptionPane.INFORMATION_MESSAGE);
                DataServerView obj = new DataServerView(server);
                obj.setLocation(getLocation());
                obj.setSize(getSize());
                obj.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(frame, "No tienes permisos para crear clientes", "ERROR PERMISOS", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(frame, "Falta el campo WEB", "Datos Incompletos", JOptionPane.ERROR_MESSAGE);
        }                
    }//GEN-LAST:event_jButton2MouseClicked

