    private void setFilas() {
        DefaultTableModel modelo = (DefaultTableModel) ProductTable.getModel();
        DataProduct producti;
        for(int i=0; i<products.size();i++){
            producti=(DataProduct)products.elementAt(i);
            Object[] datos = {producti.name}; 
            modelo.addRow(datos);
        }

        ButtonColumn buttonColumn0 = new ButtonColumn(ProductTable, see, 1);
        buttonColumn0.setMnemonic(KeyEvent.VK_D);
    }    
    
